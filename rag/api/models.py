class SessionCreateRequest(BaseModel):
    """Request to create a new session"""
    initial_query: Optional[str] = Field(None, description="Initial query for the session")
    project_id: Optional[int] = Field(None, description="Project ID")
    filter_source_type: Optional[str] = Field(None, description="Filter by source type")
    custom_instructions: Optional[str] = Field(None, description="Custom instructions for the session")

class SessionUpdateRequest(BaseModel):
    """Request to update session context"""
    project_id: Optional[int] = Field(None, description="Project ID")
    filter_source_type: Optional[str] = Field(None, description="Filter by source type")
    custom_instructions: Optional[str] = Field(None, description="Custom instructions")


class BatchResponseItem(BaseModel):
    """Response model for individual query in batch"""
    query: str
    answer: str
    retrieval_count: int
    average_score: float
    success: bool
    error: Optional[str] = None
    processing_time_ms: int = 0
    project_id: Optional[int] = None

class BatchQueryResponse(BaseModel):
    """Response model for batch queries"""
    total_queries: int
    successful: int
    failed: int
    results: List[BatchResponseItem]
    total_processing_time_ms: int
    project_id: Optional[int] = None

class HealthResponse(BaseModel):
    """Health check response"""
    status:               str
    model:                str
    retrieval_available:  bool
    web_search_available: bool           = False
    web_search_enabled:   bool           = WEB_SEARCH_AVAILABLE
    openai_available:     bool
    memory_available:     bool           = False
    active_sessions:      int            = 0
    index_vectors:        int            = 0
    metadata_records:     int            = 0
    timestamp:            str            = ""
    version:              str            = "1.3.0"
    project_id:           Optional[int]  = None
